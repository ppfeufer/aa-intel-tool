"""
AA Intel Tool exceptions
"""

# Django
from django.utils.translation import gettext_lazy as _


class AaIntelToolException(Exception):
    """
    An exception generated by AA Intel Tool.
    """


class ParserError(AaIntelToolException):
    """
    Parser Error raised for errors within our parsers
    """

    def __init__(self, message: str) -> None:
        super().__init__(message)

        self._message = message

    @property
    def message(self) -> str:
        """
        Error message.
        """

        return self._message

    def __str__(self) -> str:
        return str(_("A parser error occurred Â» {message}")).format(
            message=self.message
        )
